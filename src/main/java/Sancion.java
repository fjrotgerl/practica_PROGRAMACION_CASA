class Sancion {
    String tipo;
    String descripcion;
    String bibliotecario;
    String socio;

    Sancion(String tipo, String descripcion, String bibliotecario, String socio) {
        this.tipo = tipo;
        this.descripcion = descripcion;
        this.bibliotecario = bibliotecario;
        this.socio = socio;
    }
}